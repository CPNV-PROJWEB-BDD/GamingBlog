<h2>Utilisation</h2>
<p>Le pistolet Ghost est souvent utilisé par les joueurs qui préfèrent jouer de manière furtive et qui veulent éviter de faire du bruit en tirant. Cependant, il est également efficace à longue distance grâce à sa précision, donc il peut être utilisé pour les combats à distance moyenne et longue.</p>

<h2>Conclusion</h2>
