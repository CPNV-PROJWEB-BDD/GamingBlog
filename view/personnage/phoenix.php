<p>Phoenix est un personnage jouable dans le jeu vidéo Valorant de Riot Games. Il est connu pour sa capacité à
    contrôler le feu et à se régénérer, ce qui lui permet de tenir une position défensive ou d'attaquer avec
    agressivité. Phoenix peut également créer des murs de flammes pour bloquer la vision de ses ennemis.</p>
<h2>Compétences de Phoenix:</h2>
<p>
    <li>Blaze - Phoenix crée un mur de flammes qui peut être utilisé pour bloquer la vision de ses ennemis et
        infliger des dégâts</li>
    <li>Curveball - Phoenix lance une boule de feu qui explose en aveuglant les ennemis</li>
    <li>Hot Hands - Phoenix projette une boule de feu qui inflige des dégâts aux ennemis et lui permet de se
        régénérer</li>
    <li>Run it Back - Phoenix se clone lui-même, en créant une version temporaire de lui-même, avec la même santé
        et la même position, qui peut être utilisée pour confondre ses ennemis</li>
</p>
<h2>Histoire de Phoenix:</h2>
<p>Phoenix est un agent de feu qui a été ressuscité à partir des cendres d'un incendie. Il est devenu le premier
    agent de VALORANT à être recruté et est maintenant au service de l'organisation. Il est impulsif et téméraire,
    mais il est également un leader naturel qui inspire la confiance de ses coéquipiers. Il est motivé par son désir
    de trouver des réponses sur ses origines et de comprendre son nouveau pouvoir.</p>
